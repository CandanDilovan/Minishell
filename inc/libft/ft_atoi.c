#include "libft.h"

int	ft_atoi(char *str)
{
	char	*s;
	int		a[4];
	long	num;

	s = (char *)str;
	a[0] = 0;
	a[2] = 1;
	num = 0;
	while (s[a[0]] == ' ' || (s[a[0]] >= 9 && s[a[0]] <= 13))
			a[0]++;
	if (s[*a] == '-' || s[*a] == '+')
	{
		if (s[*a] == '-')
			a[2] = a[2] * -1;
		a[0]++;
	}
	if (a[2] == 0)
		return (0);
	while (s[a[0]] >= '0' && s[a[0]] <= '9')
	{
		num = num * 10 + (s[a[0]] - 48);
		a[0]++;
	}
	return (num * a[2]);
}
